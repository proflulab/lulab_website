import { goodsDetail } from "../../../../utils/xiaoe";


const XIAOE_GOOD_TYPE = process.env.XIAOE_GOOD_TYPE;
const XIAOE_GOOD_ID = process.env.XIAOE_GOOD_ID;


export async function GET(req: Request) {
    const resources = [{ "type": XIAOE_GOOD_TYPE, "ids": [XIAOE_GOOD_ID], },];
    const good = await goodsDetail(resources)
    return Response.json(good);
}